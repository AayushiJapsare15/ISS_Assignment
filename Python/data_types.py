# 1. Integers (int)
# Integers are whole numbers (positive, negative, or zero).

a = 10
b = -5
c = 0

# 2. Strings (str)
# Strings are sequences of characters enclosed in quotes.
name = "Ayushi"
college = 'SPIT'

# F-Strings (Formatted Strings)
# Used to insert variables directly inside strings.

age = 20
print(f"My age is {age}")

# 3. Booleans (bool)
# Boolean values represent True or False.

is_student = True
is_logged_in = False

# 4. Lists
# Lists store multiple values in a single variable.
numbers = [1, 2, 3, 4]
names = ["Ayushi", "Aman", "Riya"]
numbers[0] = 10

# 5. Tuples 
# Tuples are like lists but cannot be changed.

coordinates = (10, 20)

# 6. Sets
# Sets store unique elements only.

unique_numbers = {1, 2, 3, 3, 4}
print(unique_numbers)   # {1, 2, 3, 4}

